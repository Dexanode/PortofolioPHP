    <td height="158"><p>SMA  Dian Andalas Padang berdiri pada tahun 2002. Terhitung mulai tanggal 20 Juli  2002 resmi sebagai sekolah swasta. SMA Dian Andalas Padang terletak di jalan  Biologi Komplek Unand Ulu Gadut Padang dari ibukota provinsi Sumatera Barat  yang tepatnya berada di Gadut. Pendiri SMA Dian Andalas Padang adalah  Dharmawanita Universitas Andalas. Anggotanya ada 11 orang yang terdiri dari  Dharmawanita yang diketuai Dr.Maerawita sampai sekarang.<strong> </strong><br />
      Pada  tahun 2002, SMA Dian Andalas hanya memiliki dua buah ruangan. Satu ruangan  untuk siswa dan ruang lainnya untuk kantor kepala sekolah dan guru. Jumlah  murid pada waktu itu sebanyak 15 orang. Pada tahun 2005 akreditasi sekolah B.<br />
      Pada  tahun 2006-2012 sekolah ini memiliki beberapa ruangan yang terdiri dari 1 ruang  kantor kepala sekolah dan guru, 2 ruangan untuk kelas X, 1 ruangan untuk kelas  XI IPA, 1 ruangan untuk kelas XI IPS, 1 ruangan untuk kelas XII IPA, 1 ruangan  untuk kelas XII IPS, 1 ruangan labor komputer, 1 ruangan perpustakaan, 1  mushalla dan 2 toilet.<br />
      Kepala  sekolah yang memimpin sekolah ini sejak didirikan adalah :</p>

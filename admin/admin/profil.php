    Lulusan  pertama SMA Dian Andalas adalah tahun 2005 dengan jumlah siswa 15 orang,  sekarang sudah 12 lulusan pada tahun ajaran 2015/2016 dengan jumlah 451 siswa.  Jumlah siswa dan siswi yang belajar di SMA dian andalas saat ini sebanyak 360  siswa. Kurikulum yang dipakai masih ktsp 2006. Pada tahun 2014 akreditasi  sekolah sudah menjadi A. </p></td>
